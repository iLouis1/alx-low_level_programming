#include <stdio.h>

/**
 * add - This will as two integers
 * @k: The first integer
 * @m: The second integer
 * Return: Addition
 */

int add(int k, int m)
{
	return (k + m);
}

/**
 * div - This will divide two integers
 * @k: The first integer
 * @m: The second integer
 * Return: Divided answer
 */

int div(int k, int m)
{
	return (k / m);
}

/**
 * mod - This is the mod of two integers
 * @k: The first integer
 * @m: The second integer
 * Return: return the final answer
 */

int mod(int k, int m)
{
	return (k % m);
}

/**
 * mul - This will multiply two integers
 * @k: The first integer
 * @m: The second integer
 * Return: return multiplied answer
 */

int mul(int k, int m)
{
	return (k * m);
}

/**
 * sub - This will subtract two integers
 * @k: The first integer
 * @m: The second integer
 * Return: The subtracted result
 */

int sub(int k, int m)
{
	return (k - m);
}

